class Config:
    '''
    Configuration class.
    '''

    DEBUG_LEVEL = 1
    '''
    Debug level.
    0: No debug
    1: Debug -> Errors / warnings
    2: Debug + verbose
    '''

    identSpace = '    '
    '''
    Space to use for identation. (4 spaces or so)
    Set to None to disable identation and show all the tags on one line.
    '''